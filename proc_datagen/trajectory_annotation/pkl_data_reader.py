"""
Utilities to read and extract your trajectory PKLs generated by `get_many_trajs.py`.

Your writer code dumps `interleave_traj`:
  - action-ish dicts: {'action': <name>, 'payload': <str>} and also {'pick': id}, {'place': id}, ...
  - observations: `obs` returned by env.step([{}]) which (in your logs) looks like `list[dict]` with
        obs[0]['person']['sensors']['floating']['rgba'/'depth'/...]
  - extra strings and overlay images (`result_image`) inserted between actions/obs

This module converts that mixed stream into standardized, per-OBS steps.
It also optionally exports modalities (rgb/depth/seg/pointcloud) to disk and returns an index list.
"""

from __future__ import annotations

import json
from dataclasses import dataclass
from pathlib import Path
from typing import Any, Dict, Iterable, Iterator, List, Literal, Optional, Sequence, Tuple, Union

import numpy as np
import re

try:
    from PIL import Image
except Exception:  # pragma: no cover
    Image = None  # type: ignore


ObsType = Union[Dict[str, Any], List[Any], Tuple[Any, ...]]
Modality = Literal[
    "rgb",
    "rgba",
    "depth",
    "instance_seg",
    "semantic_seg",
    "normals",
    "pointcloud",
    "overlay",
]


@dataclass
class ExtractConfig:
    # which agent/sensor to extract from obs
    agent: str = "person"
    sensor: str = "floating"

    # what to export
    modalities: Tuple[Modality, ...] = ("rgb",)

    # image export behavior
    save_images: bool = True
    save_arrays: bool = True  # depth/seg/pcd as .npy
    save_vis: bool = True  # depth/seg as quick visualization png
    # resize RGB saved PNG to (width, height); set None to disable
    rgb_resize_wh: Optional[Tuple[int, int]] = None

    # size filter for "image-like" ndarrays when we meet loose arrays
    min_image_pixels: int = 64 * 64


def load_pickle(path: Union[str, Path]) -> Any:
    import pickle

    p = Path(path)
    with p.open("rb") as f:
        return pickle.load(f)


def _is_obs_like(x: Any) -> bool:
    """Heuristic: your obs is typically `list[dict]` and the env0 dict contains 'person'."""
    if isinstance(x, dict):
        return "person" in x or "robots" in x or "agents" in x
    if isinstance(x, (list, tuple)) and len(x) > 0 and isinstance(x[0], dict):
        d0 = x[0]
        return "person" in d0 or "robots" in d0 or "agents" in d0
    return False


def _is_action_like_dict(d: Any) -> bool:
    if not isinstance(d, dict) or not d:
        return False
    if "action" in d and "payload" in d:
        return True
    # single-key action-ish events you append in get_many_trajs.py
    if len(d) == 1:
        k = next(iter(d.keys()))
        return k in {
            "nav_to",
            "pick",
            "place",
            "open",
            "close",
            "gaze_at",
            "walkaround",
            "show_objects",
            "show_receptacles",
        }
    return False


def _maybe_env0(obs: Any) -> Any:
    """Your env is env_num=1, so obs is usually a list and env0 is obs[0]."""
    if isinstance(obs, (list, tuple)) and len(obs) > 0:
        return obs[0]
    return obs


def _get_sensors(obs: Any, *, agent: str, sensor: str) -> Optional[Dict[str, Any]]:
    d0 = _maybe_env0(obs)
    if not isinstance(d0, dict):
        return None
    a = d0.get(agent)
    if not isinstance(a, dict):
        return None
    sensors = a.get("sensors")
    if not isinstance(sensors, dict):
        return None
    s = sensors.get(sensor)
    if not isinstance(s, dict):
        return None
    return s


def _as_uint8_rgb(x: np.ndarray) -> Optional[np.ndarray]:
    if x.ndim != 3 or x.shape[2] not in (3, 4):
        return None
    y = x.astype(np.float32)
    if np.nanmax(y) <= 1.0:
        y *= 255.0
    y = np.clip(y, 0, 255).astype(np.uint8)
    return y[:, :, :3]


def _vis_2d_to_uint8(x: np.ndarray) -> Optional[np.ndarray]:
    if x.ndim != 2:
        return None
    y = x.astype(np.float32)
    mn = float(np.nanmin(y))
    mx = float(np.nanmax(y))
    if not np.isfinite(mn) or not np.isfinite(mx) or mx == mn:
        return None
    y = (y - mn) / (mx - mn + 1e-6)
    y = (y * 255.0).clip(0, 255).astype(np.uint8)
    return y


def _save_png(path: Path, arr_uint8: np.ndarray, *, resize_wh: Optional[Tuple[int, int]] = None) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    if Image is None:
        raise RuntimeError("PIL is required to save png, but Pillow is not available in this environment.")
    img = Image.fromarray(arr_uint8)
    if resize_wh is not None:
        # PIL uses (width, height)
        img = img.resize(tuple(resize_wh), resample=Image.BILINEAR)
    img.save(path)


def _save_npy(path: Path, arr: np.ndarray) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    np.save(path, arr)


def _extract_from_sensors(sensors: Dict[str, Any], modality: Modality) -> Optional[np.ndarray]:
    """Return a numpy array for a specific modality from `floating` sensor dict."""
    if modality == "rgba":
        v = sensors.get("rgba")
        return v if isinstance(v, np.ndarray) else None
    if modality == "rgb":
        v = sensors.get("rgba")
        if isinstance(v, np.ndarray):
            return _as_uint8_rgb(v)
        return None
    if modality == "depth":
        v = sensors.get("depth")
        return v if isinstance(v, np.ndarray) else None
    if modality == "normals":
        v = sensors.get("normals")
        return v if isinstance(v, np.ndarray) else None

    # segmentations may be dict with {"data": ndarray, "info": ...}
    if modality == "instance_seg":
        v = sensors.get("instance_id_segmentation")
        if isinstance(v, dict):
            vv = v.get("data")
            return vv if isinstance(vv, np.ndarray) else None
        return v if isinstance(v, np.ndarray) else None
    if modality == "semantic_seg":
        v = sensors.get("semantic_segmentation")
        if isinstance(v, dict):
            vv = v.get("data")
            return vv if isinstance(vv, np.ndarray) else None
        return v if isinstance(v, np.ndarray) else None

    if modality == "pointcloud":
        for k in ("pointcloud", "point_cloud", "pcd"):
            v = sensors.get(k)
            if isinstance(v, np.ndarray):
                return v
        return None

    return None


def interleave_traj_to_steps(
    traj_obj: Any,
    *,
    cfg: Optional[ExtractConfig] = None,
    save_root: Optional[Union[str, Path]] = None,
    episode_id: Optional[str] = None,
) -> List[Dict[str, Any]]:
    """Convert `interleave_traj` (mixed stream) into per-OBS steps.

    Returns a list of step dicts. Each step has:
      - actions: list[dict] collected since last obs
      - texts: list[str] collected since last obs
      - obs: raw obs object (usually list[dict])
      - assets: modality -> saved path (if save_root is provided)
    """
    cfg = cfg or ExtractConfig()
    save_root_p = Path(save_root) if save_root is not None else None
    if save_root_p is not None and episode_id:
        save_root_p = save_root_p / str(episode_id)

    if not isinstance(traj_obj, list):
        # fallback: treat as one-step raw
        return [{"raw": traj_obj, "actions": [], "texts": [], "obs": None, "assets": {}}]

    pending_actions: List[Dict[str, Any]] = []
    pending_texts: List[str] = []
    pending_overlays: List[np.ndarray] = []

    steps: List[Dict[str, Any]] = []

    def flush_to_step(obs: Any) -> None:
        step_id = len(steps)
        assets: Dict[str, str] = {}

        sensors = _get_sensors(obs, agent=cfg.agent, sensor=cfg.sensor)
        if sensors is not None and save_root_p is not None:
            base = save_root_p / "steps" / f"{step_id:06d}"
            for m in cfg.modalities:
                if m == "overlay":
                    continue
                arr = _extract_from_sensors(sensors, m)
                if arr is None:
                    continue
                if m in ("rgb", "rgba", "normals") and cfg.save_images:
                    # rgb is already uint8; rgba/normals may not be, but rgba is usually uint8 in your data
                    if m == "rgb":
                        _save_png(base / "rgb.png", arr, resize_wh=cfg.rgb_resize_wh)
                        assets["rgb"] = str((base / "rgb.png").as_posix())
                    elif m == "rgba":
                        rgb = _as_uint8_rgb(arr)
                        if rgb is not None:
                            _save_png(base / "rgb_from_rgba.png", rgb)
                            assets["rgb"] = str((base / "rgb_from_rgba.png").as_posix())
                        _save_npy(base / "rgba.npy", arr) if cfg.save_arrays else None
                        if cfg.save_arrays:
                            assets["rgba_npy"] = str((base / "rgba.npy").as_posix())
                    elif m == "normals":
                        # normals often float; save raw and also a quick vis if looks like [-1,1] or [0,1]
                        if cfg.save_arrays:
                            _save_npy(base / "normals.npy", arr)
                            assets["normals_npy"] = str((base / "normals.npy").as_posix())
                        if cfg.save_images and arr.ndim == 3 and arr.shape[2] in (3, 4):
                            vis = arr.astype(np.float32)
                            # heuristic: map to [0,255]
                            if np.nanmin(vis) < 0:
                                vis = (vis + 1.0) / 2.0
                            vis = (vis * 255.0).clip(0, 255).astype(np.uint8)
                            _save_png(base / "normals_vis.png", vis[:, :, :3])
                            assets["normals_vis"] = str((base / "normals_vis.png").as_posix())
                elif m in ("depth", "instance_seg", "semantic_seg") and isinstance(arr, np.ndarray):
                    if cfg.save_arrays:
                        _save_npy(base / f"{m}.npy", arr)
                        assets[f"{m}_npy"] = str((base / f"{m}.npy").as_posix())
                    if cfg.save_vis and cfg.save_images:
                        vis2d = _vis_2d_to_uint8(arr)
                        if vis2d is not None:
                            _save_png(base / f"{m}_vis.png", vis2d)
                            assets[f"{m}_vis"] = str((base / f"{m}_vis.png").as_posix())
                elif m == "pointcloud" and isinstance(arr, np.ndarray):
                    if cfg.save_arrays:
                        _save_npy(base / "pointcloud.npy", arr)
                        assets["pointcloud_npy"] = str((base / "pointcloud.npy").as_posix())

        # handle overlays saved from pending_overlays
        if save_root_p is not None and pending_overlays:
            base = save_root_p / "steps" / f"{step_id:06d}"
            for j, ov in enumerate(pending_overlays[:5]):  # cap
                if not isinstance(ov, np.ndarray):
                    continue
                if ov.ndim == 3 and ov.shape[2] in (3, 4) and cfg.save_images:
                    rgb = _as_uint8_rgb(ov)
                    if rgb is None:
                        continue
                    p = base / f"overlay_{j}.png"
                    _save_png(p, rgb)
                    assets[f"overlay_{j}"] = str(p.as_posix())
                elif cfg.save_arrays:
                    p = base / f"overlay_{j}.npy"
                    _save_npy(p, ov)
                    assets[f"overlay_{j}_npy"] = str(p.as_posix())

        steps.append(
            {
                "step_id": step_id,
                "actions": list(pending_actions),
                "texts": list(pending_texts),
                "obs": obs,
                "assets": assets,
                # compatibility: pick one canonical image path if available
                "obs_image": assets.get("rgb") or assets.get("depth_vis") or None,
                "obs_text": {
                    "actions": list(pending_actions),
                    "texts": list(pending_texts),
                },
            }
        )

    for item in traj_obj:
        if _is_action_like_dict(item):
            pending_actions.append(item)
            continue
        if isinstance(item, str):
            if item.strip():
                pending_texts.append(item.strip())
            continue
        if isinstance(item, np.ndarray):
            # could be overlay image or some other array dumped into traj
            if item.size >= cfg.min_image_pixels and (item.ndim == 2 or (item.ndim == 3 and item.shape[2] in (3, 4))):
                pending_overlays.append(item)
            else:
                pending_texts.append(f"[ndarray skipped shape={item.shape} dtype={item.dtype}]")
            continue
        if _is_obs_like(item):
            flush_to_step(item)
            pending_actions.clear()
            pending_texts.clear()
            pending_overlays.clear()
            continue

        # unknown item type
        pending_texts.append(f"[unknown item type: {type(item)}]")

    # if trajectory ends with pending but no obs, keep a tail record
    if pending_actions or pending_texts or pending_overlays:
        steps.append(
            {
                "step_id": len(steps),
                "actions": list(pending_actions),
                "texts": list(pending_texts),
                "obs": None,
                "assets": {},
                "obs_image": None,
                "obs_text": {"actions": list(pending_actions), "texts": list(pending_texts)},
            }
        )

    return steps


def export_episode_jsonl(
    traj_path: Union[str, Path],
    *,
    out_dir: Union[str, Path],
    cfg: Optional[ExtractConfig] = None,
    episode_id: Optional[str] = None,
) -> Path:
    """One-call helper: load pkl -> steps -> save assets -> write index.jsonl."""
    cfg = cfg or ExtractConfig()
    traj_path = Path(traj_path)
    out_dir = Path(out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    if episode_id is None:
        episode_id = traj_path.stem

    traj_obj = load_pickle(traj_path)
    steps = interleave_traj_to_steps(traj_obj, cfg=cfg, save_root=out_dir, episode_id=episode_id)

    index_path = out_dir / str(episode_id) / "index.jsonl"
    index_path.parent.mkdir(parents=True, exist_ok=True)
    with index_path.open("w", encoding="utf-8") as f:
        for s in steps:
            rec = {
                "episode_id": str(episode_id),
                "step_id": int(s.get("step_id", -1)),
                "actions": s.get("actions", []),
                "texts": s.get("texts", []),
                "assets": s.get("assets", {}),
            }
            f.write(json.dumps(rec, ensure_ascii=False) + "\n")
    return index_path


_CATEGORY_RE = re.compile(r"\bcategory\s+([A-Za-z0-9_]+)\b", re.IGNORECASE)


def _normalize_action_dict(d: Dict[str, Any]) -> Optional[Dict[str, Any]]:
    """Normalize your stored action-ish dict into a more structured schema.

    Examples (input -> output):
      {'action':'nav_to','payload':'table_3'} -> {'nav_to': {'receptacle_name':'table_3'}}
      {'pick': '1'} -> {'pick': {'marker_id':'1'}}
      {'gaze_at': 0} -> {'gaze_at': {'marker_id':'0'}}
      {'show_objects': 'Need to pick up the object of category bread'} -> {'show_object_by_category': {'target_category':'bread'}}
      {'show_receptacles': 'Need to place the object on desk_1'} -> {'show_receptacles': ''}
      {'walkaround': '...a bread...'} -> {'walk_around': {'text': '...a bread...'}}
    """
    if not _is_action_like_dict(d):
        return None

    # canonical action/payload wrapper
    if "action" in d and "payload" in d:
        act = d.get("action")
        payload = d.get("payload")
        if act == "nav_to":
            return {"nav_to": {"receptacle_name": str(payload)}}
        return {str(act): payload}

    if len(d) != 1:
        return d

    k = next(iter(d.keys()))
    v = d[k]

    if k in {"pick", "place", "open", "close", "gaze_at"}:
        return {k: {"marker_id": str(v)}}

    if k == "show_receptacles":
        # in your desired format you only need the marker panel event itself
        return {"show_receptacles": ""}

    if k == "show_objects":
        # try to recover category from the text you stored
        if isinstance(v, str):
            m = _CATEGORY_RE.search(v)
            if m:
                return {"show_object_by_category": {"target_category": m.group(1)}}
        return {"show_objects": v}

    if k == "walkaround":
        # normalize naming to your sample
        if isinstance(v, str):
            return {"walk_around": {"text": v}}
        return {"walk_around": v}

    # nav_to sometimes could appear as single-key dict in other versions
    if k == "nav_to":
        if isinstance(v, dict):
            name = v.get("name") or v.get("receptacle_name") or v.get("target")
            if name is not None:
                return {"nav_to": {"receptacle_name": str(name)}}
        return {"nav_to": v}

    return {k: v}


def extract_action_sequence(traj_obj: Any) -> List[Dict[str, Any]]:
    """Extract an action-only step sequence from your `interleave_traj` PKL.

    This answers: "有没有 step_id 来确定分点？"
      - PKL does NOT store an explicit step_id on action events.
      - We generate step_id in-order as we traverse the interleave list.

    Returns:
      [
        {"step_id": 0, "event": {...}},
        {"step_id": 1, "event": {...}},
        ...
      ]
    """
    if not isinstance(traj_obj, list):
        return []

    out: List[Dict[str, Any]] = []
    for item in traj_obj:
        if isinstance(item, dict):
            ev = _normalize_action_dict(item)
            if ev is not None:
                out.append({"step_id": len(out), "event": ev})
            continue
        # ignore obs/arrays/strings here by design
    return out


def format_step_sequence(seq: Sequence[Dict[str, Any]]) -> str:
    """Pretty format the output of `extract_action_sequence()` into human-readable text."""
    lines: List[str] = []
    for rec in seq:
        sid = rec.get("step_id")
        ev = rec.get("event")
        lines.append(f"Step {sid}: {json.dumps(ev, ensure_ascii=False)}")
    return "\n".join(lines)


def _make_template_record(
    *,
    model_name: str,
    eval_subset: str,
    episode_id: str,
    step_id: int,
    obs_text: Any,
    obs_image: str,
    system_prompt: str = "",
    human_prompt: str = "",
    gpt_value: str = "",
    think_or_action: int = 0,
    end_turn: bool = False,
) -> Dict[str, Any]:
    return {
        "metadata": {
            "model_name": str(model_name),
            "eval_subset": str(eval_subset),
            "episode_id": str(episode_id),
            "step_id": int(step_id),
        },
        "obs_text": obs_text,
        "obs_image": obs_image,
        "conversations": [
            {"from": "system", "value": system_prompt},
            {"from": "human", "value": human_prompt},
            {"from": "gpt", "value": gpt_value, "think_or_action": int(think_or_action), "end_turn": bool(end_turn)},
        ],
    }


def export_traj_to_template_records(
    traj_obj: Any,
    *,
    out_root: Union[str, Path],
    episode_id: str,
    model_name: str = "gpt-5.2-1228-test",
    eval_subset: str = "test",
    cfg: Optional[ExtractConfig] = None,
    include_string_events: bool = True,
) -> List[Dict[str, Any]]:
    """Export ONE trajectory object to template-style JSON records.

    Step definition here is **action-centered**:
      - each action-like dict becomes a step (with step_id in order)
      - we attach the *first following* RGB observation before the next action as obs_image (if exists)
      - optionally, plain strings become steps too (obs_text={"text": ...})

    Images are written to:
      {out_root}/obs/images/{episode_id}/{episode_id}_step_{step_id}.png
    """
    cfg = cfg or ExtractConfig(modalities=("rgb",))
    out_root = Path(out_root)
    img_dir = out_root / "obs" / "images" / str(episode_id)
    img_dir.mkdir(parents=True, exist_ok=True)

    if not isinstance(traj_obj, list):
        return []

    # collect "events" in order; each event becomes one step
    events: List[Dict[str, Any]] = []
    for item in traj_obj:
        if isinstance(item, dict):
            ev = _normalize_action_dict(item)
            if ev is not None:
                events.append({"kind": "event", "event": ev})
            continue
        if include_string_events and isinstance(item, str) and item.strip():
            events.append({"kind": "text", "event": {"text": item.strip()}})
            continue

    # now, for each event, scan forward in the original stream to find the first obs before next event
    records: List[Dict[str, Any]] = []

    # precompute indices of event positions in the raw stream for accurate "lookahead"
    raw_event_idx: List[int] = []
    for i, item in enumerate(traj_obj):
        if isinstance(item, dict) and _normalize_action_dict(item) is not None:
            raw_event_idx.append(i)
        elif include_string_events and isinstance(item, str) and item.strip():
            raw_event_idx.append(i)

    def find_first_rgb_between(i0: int, i1_exclusive: int) -> Optional[np.ndarray]:
        for j in range(i0 + 1, i1_exclusive):
            it = traj_obj[j]
            if _is_obs_like(it):
                sensors = _get_sensors(it, agent=cfg.agent, sensor=cfg.sensor)
                if sensors is None:
                    continue
                rgb = _extract_from_sensors(sensors, "rgb")
                if isinstance(rgb, np.ndarray):
                    return rgb
        return None

    for step_id, ev in enumerate(events):
        obs_text = ev["event"]
        obs_image_path = ""

        # map step_id -> raw index window
        if step_id < len(raw_event_idx):
            start_raw = raw_event_idx[step_id]
            end_raw = raw_event_idx[step_id + 1] if (step_id + 1) < len(raw_event_idx) else len(traj_obj)
            rgb = find_first_rgb_between(start_raw, end_raw)
            if rgb is not None and cfg.save_images:
                img_path = img_dir / f"{episode_id}_step_{step_id}.png"
                _save_png(img_path, rgb.astype(np.uint8), resize_wh=cfg.rgb_resize_wh)
                obs_image_path = str(img_path.as_posix())

        records.append(
            _make_template_record(
                model_name=model_name,
                eval_subset=eval_subset,
                episode_id=str(episode_id),
                step_id=step_id,
                obs_text=obs_text,
                obs_image=obs_image_path,
            )
        )

    return records


def export_scene_to_template_json(
    *,
    scene_dir: Union[str, Path],
    out_root: Union[str, Path],
    out_json: Union[str, Path],
    model_name: str = "gpt-5.2-1228-test",
    eval_subset: str = "test",
    cfg: Optional[ExtractConfig] = None,
    only_layout_ids: Optional[Iterable[int]] = None,
    episode_id_prefix: str = "ep_",
    start_episode_index: int = 1,
) -> Path:
    """Batch export many `layout*_traj.pkl` into ONE JSON list (template format).

    Episode IDs will be: ep_000001, ep_000002, ...
    """
    scene_dir = Path(scene_dir)
    out_root = Path(out_root)
    out_json = Path(out_json)

    pkls = sorted(scene_dir.glob("layout*_traj.pkl"))
    if only_layout_ids is not None:
        allow = set(int(x) for x in only_layout_ids)
        def _lid(p: Path) -> Optional[int]:
            m = re.search(r"layout(\d+)_traj\.pkl$", p.name)
            return int(m.group(1)) if m else None
        pkls = [p for p in pkls if (_lid(p) in allow)]

    all_records: List[Dict[str, Any]] = []
    ep_idx = start_episode_index
    for p in pkls:
        episode_id = f"{episode_id_prefix}{ep_idx:06d}"
        traj_obj = load_pickle(p)
        recs = export_traj_to_template_records(
            traj_obj,
            out_root=out_root,
            episode_id=episode_id,
            model_name=model_name,
            eval_subset=eval_subset,
            cfg=cfg,
        )
        all_records.extend(recs)
        ep_idx += 1

    out_json.parent.mkdir(parents=True, exist_ok=True)
    out_json.write_text(json.dumps(all_records, ensure_ascii=False, indent=2), encoding="utf-8")
    return out_json


